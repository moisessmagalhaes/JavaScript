let exp = Math.pow(2,3); //O primeiro parâmetro é a base se o segundo é o expoente.
console.log('exp: ', exp); //2 elevado a 3 igual a 8

let exp2 = 2 ** 3; //O primeiro parâmetro é a base se o segundo é o expoente.
console.log('exp2: ', exp2);

let base = 2;
base **=3; //Eleva o valor armazenado na base a terceira potência.
console.log('base: ', base); //2 elevado a 3 igual a 8

let exp3 = 2 ** -3; //com expoente negativo
console.log('exp3: ', exp3);
